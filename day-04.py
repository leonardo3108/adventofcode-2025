input = open('input-04.txt').read()

grid = [list(line) for line in input.strip().split('\n')]
rows = len(grid)
cols = len(grid[0])
total_count = 0
passing = 1

while True:
    new_grid = [row.copy() for row in grid]
    accessible_count = 0
    for y in range(rows):
        for x in range(cols):
            if grid[y][x] == '@':
                adjacent_rolls = 0
                for dy in [-1, 0, 1]:
                    for dx in [-1, 0, 1]:
                        if dy == 0 and dx == 0:
                            continue
                        ny, nx = y + dy, x + dx
                        if 0 <= ny < rows and 0 <= nx < cols:
                            if grid[ny][nx] == '@':
                                adjacent_rolls += 1
                if adjacent_rolls < 4:
                    accessible_count += 1
                    new_grid[y][x] = '.'

    # for row in grid: print(''.join(row))

    if passing == 1:
        print("Part One - Total rolls of paper accessible by forklifts - first check:", accessible_count)
    else:
        pass#print(f'\tRolls of paper accessible by forklifts - {passing} check: {accessible_count}')
    total_count += accessible_count
    if accessible_count == 0:
        break
    accessible_count = 0
    grid = new_grid
    passing += 1

print("Part Two - Total rolls of paper accessible by forklifts -  all checks:", total_count)
